public class EjWhile {

    /**
     * @param args the command line arguments
     */
    public static void main(String[] args) {
        int numero = 0;
        while (numero !=10){
            System.out.println("El numero es distinto de cero" + numero);
            --numero;
        }
        System.out.println("FINAL");
    }

}
